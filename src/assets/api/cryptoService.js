import client from './client.js'

export const getTopListCrtpro = async () => {
  let url = `/asset/v1/top/list?page=${1}&page_size=${30}&sort_by=${'CIRCULATING_MKT_CAP_USD'}&sort_direction=${'DESC'}&groups=${'ID,BASIC,SUPPLY,PRICE,MKT_CAP,VOLUME,CHANGE,TOPLIST_RANK'}&toplist_quote_asset=${'USD'}`
  const result = (await client.get(url)).Data.LIST
  return result
}

export const searchCrypto = async (query) => {
  let url = `/asset/v1/search?search_string=${query}&limit=${10}`
  const searchResult = (await client.get(url)).Data.LIST

  return await getCryptosByIDs(searchResult)
}

export const getCryptosByIDs = async (list) => {
  const string_list = list.map((i) => i.ID).join(',')
  let url = `/asset/v2/metadata?assets=${string_list}&asset_lookup_priority=ID&quote_asset=USD&asset_language=en-US`
  const result = (await client.get(url)).Data
  return Object.values(result)
}

